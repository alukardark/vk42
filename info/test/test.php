<h4><span style="color: #d22e31;"><u>Как накапливать бонусы на карту «ВК-БОНУС»?</u></span> </h4>
 <b>1 бонус на карте = 1 рубль </b><br>
 <br>
 Бонусы предоставляются во всех сервис-центрах «ВК» на товары и услуги. <br>
 <br>
 Стандартно бонусы начисляются в размере 4% от стоимости товара и/или суммы оплаченных услуг*. <br>
 <br>
 На товары и/или услуги по специальным ценам бонусы начисляются в размере 1,5% от стоимости товара и/или суммы оплаченных услуг*. <br>
 <br>
 Бонусные баллы могут быть удвоены при покупке (разово) в следующих случаях: <br>
<ul>
	<li>день рождения Клиента (в течение месяца со дня рождения). </li>
	<li>календарные праздники, в том числе отраслевые и общепринятые. </li>
	<li>иные маркетинговые активности. </li>
</ul>
 Также процент бонусов, начисленных от суммы покупки и/или услуги, может меняться в зависимости от проводимых маркетинговых акций. <br>
 <br>
