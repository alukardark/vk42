<h4><span style="color: #d22e31;"><u>Как тратить бонусы на карте «ВК-БОНУС»?</u></span> </h4>
 Накопленными бонусами Вы можете оплатить часть стоимости покупки и/или услуги, но не более 50% от общей суммы покупки (на товар до 50%, на услуги до 25%), включая товары/услуги по специальным ценам. <br>
 <br>
 Оплата бонусами товара может производиться только при следующей покупке (на следующие сутки). <br>
 <br>
 Оплата бонусами услуг может производиться сразу в момент начисления бонусов.<br>
 <br>
 В случае если по бонусной карте не было произведено никаких действий (начисление либо снятие) в течение 2-х лет подряд, бонусная карта прекращает свое действие. <br>
 <br>
 Накопленные бонусы на карте имеют срок давности один календарный год. По истечению данного срока с момента зачисления неиспользованные бонусы списываются (обнуляются).<br>
